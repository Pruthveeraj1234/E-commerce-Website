let sliderIndexs = 0;

function moveSliders(direction) {
  const slide = document.querySelectorAll(".slider-items");
  const totalSlide = slide.length;
  const slidersWidth = slide[0].offsetWidth;
  sliderIndexs = (sliderIndexs + direction + totalSlide) % totalSlide;
  const movements = -sliderIndexs * slidersWidth;
  document.querySelector(
    ".sliders"
  ).style.transform = `translateX(${movements}px)`;
}

